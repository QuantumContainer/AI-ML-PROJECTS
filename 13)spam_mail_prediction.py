import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score
from sklearn.feature_extraction.text import TfidfVectorizer

dataset = pd.read_csv('/content/mail_data.csv')
dataset.head()

dataset = dataset.where((pd.notnull(dataset)),'') # replacing the null values with a null string

dataset.isnull().sum()

dataset.replace({'Category':{'spam':0,'ham':1}},inplace=True)
dataset.head()

X = dataset['Message']
Y = dataset['Category']
print(X)
print(Y)

X_train,X_test,Y_train,Y_test = train_test_split(X,Y,test_size=0.2,random_state=42)
print(X.shape,X_train.shape,X_test.shape)

feature_extraction = TfidfVectorizer(min_df=1,stop_words='english',lowercase=True)
X_train_features = feature_extraction.fit_transform(X_train)
X_test_features = feature_extraction.transform(X_test)

y_train = Y_train.astype('int')
y_test = Y_test.astype('int')

print(X_train_features)

model = LogisticRegression()
model.fit(X_train_features,y_train)

prediction_on_training_data = model.predict(X_train_features)
accuracy_on_training_data = accuracy_score(y_train,prediction_on_training_data)
print('Accuracy on training data : ',accuracy_on_training_data)

prediction_on_test_data = model.predict(X_test_features)
accuracy_on_test_data = accuracy_score(y_test,prediction_on_test_data)
print('Accuracy on test data : ',accuracy_on_test_data)

from sklearn.metrics import confusion_matrix
import seaborn as sns
import matplotlib.pyplot as plt

cm = confusion_matrix(y_test, prediction_on_test_data)

plt.figure(figsize=(6, 4))
sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', xticklabels=['Spam', 'Ham'], yticklabels=['Spam', 'Ham'])
plt.xlabel('Predicted Label')
plt.ylabel('True Label')
plt.title('Confusion Matrix')
plt.show()

input_mail = [input('Enter your message: ')]

# converting the text to feature vectors
input_data_features = feature_extraction.transform(input_mail)

# making predictions
prediction = model.predict(input_data_features)

if (prediction[0]==1):
  print('Ham mail')
else:
  print('Spam mail')

import joblib

# Save the trained model
joblib.dump(model, 'logistic_regression_model.pkl')

# Save the TfidfVectorizer as it's needed for transforming new input data
joblib.dump(feature_extraction, 'tfidf_vectorizer.pkl')

print('Model and TF-IDF vectorizer saved successfully!')
